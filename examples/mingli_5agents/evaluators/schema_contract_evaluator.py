def schema_contract_score(result: dict[str, Any], expected: dict[str, Any] | None = None) -> float:
    """Score whether public schema output covers core API/audit contracts."""
    from examples.mingli_5agents.api_core import schema_document

    document = schema_document()
    schemas = document.get("schemas", {})
    endpoints = document.get("endpoints", {})
    provider_protocols = document.get("provider_protocols", {})

    checks = [
        _schemas_present(schemas),
        _response_refs_ok(schemas),
        _required_fields_ok(schemas),
        _endpoint_docs_ok(endpoints),
        _provider_protocols_ok(provider_protocols),
    ]
    return round(sum(checks) / len(checks), 6)


def _schemas_present(schemas: object) -> float:
    if not isinstance(schemas, dict):
        return 0.0
    present = REQUIRED_SCHEMAS & set(schemas)
    return len(present) / len(REQUIRED_SCHEMAS)


