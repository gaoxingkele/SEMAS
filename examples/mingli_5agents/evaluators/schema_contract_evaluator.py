def _ref(schemas: dict[str, Any], schema_name: str, path: str) -> str | None:
    current: Any = schemas.get(schema_name)
    for part in path.split("."):
        if part == "items":
            current = current.get("items") if isinstance(current, dict) else None
        elif part == "additionalProperties":
            current = current.get("additionalProperties") if isinstance(current, dict) else None
        else:
            current = current.get("properties", {}).get(part) if isinstance(current, dict) else None
        if current is None:
            return None
    return current.get("$ref") if isinstance(current, dict) else None


def _schema_required(schemas: dict[str, Any], schema_name: str, path: str, required: set[str]) -> bool:
    current: Any = schemas.get(schema_name)
    for part in path.split("."):
        if part == "items":
            current = current.get("items") if isinstance(current, dict) else None
        elif part == "additionalProperties":
            current = current.get("additionalProperties") if isinstance(current, dict) else None
        else:
            current = current.get("properties", {}).get(part) if isinstance(current, dict) else None
        if current is None:
            return False
    fields = current.get("required") if isinstance(current, dict) else None
    return isinstance(fields, list) and required.issubset(set(fields))


def _anyof_ref(schemas: dict[str, Any], schema_name: str, path: str) -> str | None:
    current: Any = schemas.get(schema_name)
    for part in path.split("."):
        current = current.get("properties", {}).get(part) if isinstance(current, dict) else None
        if current is None:
            return None
    for item in current.get("anyOf", []) if isinstance(current, dict) else []:
        if isinstance(item, dict) and item.get("$ref"):
            return item["$ref"]
    return None


def _anyof_contains_ref(schemas: dict[str, Any], schema_name: str, path: str, expected_ref: str) -> bool:
    current: Any = schemas.get(schema_name)
    for part in path.split("."):
        current = current.get("properties", {}).get(part) if isinstance(current, dict) else None
        if current is None:
            return False
    return any(isinstance(item, dict) and item.get("$ref") == expected_ref for item in current.get("anyOf", []))
